public class DrawProperties {

    private int lineColour;
    private int fillColour;
    private int linePattern;
    private int lineWidth;

    public int getLineWidth() {
        return lineWidth;
    }

    public void setLineWidth(int lineWidth) {
        this.lineWidth = lineWidth;
    }

    public int getLineColour() {
        return lineColour;
    }

    public void setLineColour(int lineColour) {
        this.lineColour = lineColour;
    }

    public int getFillColour() {
        return fillColour;
    }

    public void setFillColour(int fillColour) {
        this.fillColour = fillColour;
    }

    public int getLinePattern() {
        return linePattern;
    }

    public void setLinePattern(int linePattern) {
        this.linePattern = linePattern;
    }
}
